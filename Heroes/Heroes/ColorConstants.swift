import UIKit

extension UIColor {
    // App colors
    class func boyColor() -> UIColor {
        return UIColor(red: 103/255, green: 200/255, blue: 230/255, alpha: 1)
    }
    
    class func girlColor() -> UIColor {
        return UIColor(red: 215/255, green: 160/255, blue: 238/255, alpha: 1)
    }
    
}